package br.com.adrisilva.calculadora.classes;

public class Operations {
    public static float add(float value_one, float value_two){
        return value_one + value_two;
    }
    
    public static float subtract(float value_one, float value_two){
        return value_one - value_two;
    }
    
    public static float multiply(float value_one, float value_two){
        return value_one * value_two;
    }
    
    public static float divide(float value_one, float value_two){
        return value_one / value_two;
    }
    
}
